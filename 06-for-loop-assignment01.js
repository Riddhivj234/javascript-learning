console.log("==========Numbers from 5 to 15 by incrementing by 1===========");

for (var index = 5 ; index <= 15 ; index++) {
    
    console.log(index);
    
}

console.log("====Numbers from 50 to 40 by decrementing by 1=========");

for (var index = 50 ; index >= 40 ; index--) {
    console.log(index);
    
}

